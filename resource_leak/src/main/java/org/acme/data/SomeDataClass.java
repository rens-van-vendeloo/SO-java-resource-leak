package org.acme.data;

import jakarta.enterprise.context.ApplicationScoped;

@ApplicationScoped
public class SomeDataClass {
    int id;
    String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
